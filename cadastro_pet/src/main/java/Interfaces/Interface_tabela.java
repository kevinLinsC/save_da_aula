    public void PopularJTable(String sql) {
        try {
            Connection con = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/jogos", "root", "");
            PreparedStatement banco = (PreparedStatement)con.prepareStatement(sql);
            banco.execute(); // cria o vetor
            
            ResultSet resultado = banco.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            
            while (resultado.next())
            {
                model.addRow(new Object[]
                {
                   resultado.getString("id_jogo"),
                   resultado.getString("nome_jogo"),
                   resultado.getString("genero_jogo"),
                   resultado.getString("preco_jogo")
                });
            }
            
            banco.close();
            con.close();
        }
        catch (SQLException e) {
            System.out.println("O erro foi esse Ã´" + e.getMessage());
        }
    }
